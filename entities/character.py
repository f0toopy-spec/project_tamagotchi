"""
Модуль персонажа для игры Tamagotchi Pou.
"""


class Character:
    """Представляет главного персонажа в игре Tamagotchi Pou.
    
    Этот класс отвечает за эволюцию персонажа и предметы кастомизации.
    Персонаж может эволюционировать через 3 стадии и носить различные предметы.
    """
    
    def __init__(self):
        """Инициализирует нового персонажа со значениями по умолчанию.
        
        Персонаж начинается с 1 стадии эволюции без предметов кастомизации.
        """
        self.evolution_stage = 1  # Текущая стадия эволюции (1-3)
        self.customizations = {}  # Словарь предметов кастомизации {тип: id}

    def evolve(self):
        """Эволюционирует персонажа на следующую стадию.
        
        Персонаж может эволюционировать со стадии 1 на 2, и со стадии 2 на 3.
        На 3 стадии дальнейшая эволюция невозможна.
        
        Возвращает:
            bool: True если эволюция прошла успешно, False если достигнут максимум.
        """
        if self.evolution_stage < 3:
            self.evolution_stage += 1
            return True
        return False

    def add_customization(self, item_type, item_id):
        """Добавляет предмет кастомизации персонажу.
        
        Аргументы:
            item_type: Категория предмета (например, 'шляпа', 'рубашка', 'аксессуар').
            item_id: Уникальный идентификатор предмета в рамках его типа.
        
        Новый предмет заменит существующий предмет того же типа.
        """
        self.customizations[item_type] = item_id